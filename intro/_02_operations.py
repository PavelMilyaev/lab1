def solve_equation():
    #Решение уравнения для получения 25 из 1 2 3 4 5
    result = (1 * 2 + 3) * 4 + 5
    return result

def run_operations_demo():
    #Демонстрация решения уравнения
    result = solve_equation()
    print(f"Результат уравнения (1 * 2 + 3) * 4 + 5 = {result}")
    return result

if __name__ == "__main__":
    run_operations_demo()
